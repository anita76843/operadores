#ejemplo de operaciones de comparacion
m = int(input("ingrese el valor de m:"))
n= int(input("ingrese el valor de n:"))

print(f"m y n son iguales? {m==n}")# igualdad
print(f"m es mayor que n?:{m > n}")# mayor que >
print(m < n)# menor que <
print(m >= n)# mayor igual que >=
print(m <= n)# menor igual que <=
print(m != n)# no igual !
