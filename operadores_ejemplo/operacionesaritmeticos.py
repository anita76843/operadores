# ejemplos utilizando operadores aritmeticos
numero1 = int(input("ingrese el numero1:"))
numero2 = int(input("ingrese el numero2:"))
suma = numero1 + numero2
resta = numero1 - numero2
division =numero1/numero2
multiplicacion= numero1 * numero2
modulo = numero1 % numero2
divisionentera = numero1 // numero2

print(f"la suma de {numero1} y {numero2}:", str(suma))
print(f"la resta de {numero1} y {numero2} :", str(resta))
print(f"la division de {numero1} y {numero2}:", str(division))
print(f"la multiplicacion de {numero1} y {numero2}:", str(multiplicacion))
print(f"el modulo de {numero1} y{numero2}:", str(modulo))
print(f"la division entera de {numero1} y {numero2}:", str(divisionentera))


