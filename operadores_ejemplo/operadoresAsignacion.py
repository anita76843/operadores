# ejemplos de operadores de asignacion
var1 = 5
var2 = 12
var3 = 8
var4 = 14
var5 = 6
var6 = 8

var1 += 3 #var1 + 3 asignacion con suma
var2 -= 3 #var2 - 3 asignacion con resta
var3 *= 3 #var3 / 3 asignacion con division
var4 /= 3 #var4 * 3 asignacion con multiplicacion
var5 %= 3 #var5 % 3 asignacion con modulo
var6 //= 3 #var6 // 3 asignacion con division entera
print(f"asiganacion con suma: {var1}")
print(f"asiganacion con resta: {var2}")
print(f"asiganacion con division: {var3}")
print(f"asiganacion con multiplicacion: {var4}")
print(f"asiganacion con modulo: {var5}")
print(f"asiganacion con division entera: {var6}")


